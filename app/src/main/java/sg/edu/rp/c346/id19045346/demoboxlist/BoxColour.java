public class BoxColour {
    private String colour;


    public BoxColour(String colour) {
        this.colour = colour;
    }


    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public String toString() {
        return "Box Colour: " + colour;
    }
}
